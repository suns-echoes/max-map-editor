export function hexToUint8(hex: string): Uint8Array {
	const H: Record<string, number> = { '0': 0x00, '1': 0x10, '2': 0x20, '3': 0x30, '4': 0x40, '5': 0x50, '6': 0x60, '7': 0x70, '8': 0x80, '9': 0x90, a: 0xa0, b: 0xb0, c: 0xc0, d: 0xd0, e: 0xe0, f: 0xf0, A: 0xA0, B: 0xB0, C: 0xC0, D: 0xD0, E: 0xE0, F: 0xF0 };
	const L: Record<string, number> = { '0': 0x00, '1': 0x01, '2': 0x02, '3': 0x03, '4': 0x04, '5': 0x05, '6': 0x06, '7': 0x07, '8': 0x08, '9': 0x09, a: 0x0a, b: 0x0b, c: 0x0c, d: 0x0d, e: 0x0e, f: 0x0f, A: 0x0A, B: 0x0B, C: 0x0C, D: 0x0D, E: 0x0E, F: 0x0F };
	const length = hex.length >> 1;
	const ui8 = new Uint8Array(length);
	for (let i = 0, j = 0; i < length; i++)
		ui8[i] = H[hex[j++]] | L[hex[j++]];
	return ui8;
}
